def create_icao_airports_dict():
    icao_airport_codes_dict = {}
    with open("airports.json", "r") as file:
        airport_data = json.load(file)
    for airport in airport_data:
        airport_code = airport_data[airport]["icao"]
        airport_name = airport_data[airport]["name"]
        icao_airport_codes_dict[airport_code] = airport_name
    return icao_airport_codes_dict

def create_iata_airports_dict():
    iata_airport_codes_dict = {}
    with open("airports.json", "r") as file:
        airport_data = json.load(file)
    for airport in airport_data:
        airport_code = airport_data[airport]["iata"]
        if airport_code == "" or airport_code is None:
            continue
        airport_name = airport_data[airport]["name"]
        iata_airport_codes_dict[airport_code] = airport_name
    return iata_airport_codes_dict
